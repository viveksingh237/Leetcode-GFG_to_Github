class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {   if(m==0 and n==0) return 1;
        int cnt=1;
        while(m or n){
            int bitm= !(m%2);
            int bitn= !(n%2);
            if((bitm ^ bitn)) return cnt;
            cnt++;
            m>>=1;
            n>>=1;
        }
        return -1;
    }
};

