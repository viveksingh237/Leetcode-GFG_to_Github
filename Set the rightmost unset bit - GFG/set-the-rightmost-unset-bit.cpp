class Solution
{
public:
    int setBit(int N)
    {
        int temp=1,m=N;
        while(m){
            if(!(m & 1)){
                N = N | temp;
                break;
            }
            temp <<= 1;
            m >>= 1;
        }
        return N;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans = ob.setBit(N);
        cout << ans << endl;
    }
    return 0;
}
